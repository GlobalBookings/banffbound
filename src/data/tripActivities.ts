export const hotelLink = (budget: string) => {
  const base = `https://www.expedia.ca/Hotel-Search?destination=Banff%2C+Alberta&camref=${EXP}`;
  switch(budget) {
    case 'luxury': return `${base}&sort=RECOMMENDED&star=50`;
    case 'mid': return `${base}&sort=RECOMMENDED&star=30,40`;
    case 'budget': return `${base}&sort=PRICE_LOW_TO_HIGH`;
    default: return base;
  }
};
