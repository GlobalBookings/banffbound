export function getRestaurantsByArea(area: Area): Restaurant[] {
  return restaurants.filter(r => r.area === area);
}

/** Get all unique areas that have restaurants */
export function getAreas(): Area[] {
  return [...new Set(restaurants.map(r => r.area))] as Area[];
}
